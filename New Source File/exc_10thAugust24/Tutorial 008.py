# What is a Datatype
# Data Type is a Type of Variable / Container
# Python Data-Types are theclasification or catergaorization of the data items.
# It represent the kind of values that tells what operations cn e performa on the particular data.

# Text Type -> str
x = "Amita Anant Jadhav"
#display x:
print(x)
#display the data type of x:
print(type(x))
print("------------------------------------------------------------------")
# Numeric Type -> int
x = 1890
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Numeric Type -> float
x = 18.99
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Numeric Type -> complex number / imaginary number / iota
x = 1j
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Sequence Type -> list
x = ["Banana","Apple","Mango"]
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Sequence Type -> Tuple
x = ("Banana","Apple","Mango")
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Sequence Type -> Range
x = range(6)
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Mapping Type -> Dictionary
x = {"Name":"Amita" , "Age":"32"}
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Set Type -> Set
x = {"Name","Amita","Age","32"}
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Set Type -> Frozen Set
x = frozenset({"Name","Amita","Age","32"})
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Boolean Type
x = True
print(x)
print(type(x))
y = False
print(y)
print(type(y))
print("------------------------------------------------------------------")
# Binary Type -> bytes
x = b"Hello"
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Binary Type -> bytearray
x = bytearray(5)
print(x)
print(type(x))
print("------------------------------------------------------------------")
# Binary Type -> bytearray
x = memoryview(bytes(5))
print(x)
print(type(x))
print("------------------------------------------------------------------")
x = None
print(x)
print(type(x))



