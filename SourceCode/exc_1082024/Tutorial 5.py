# Maximum Number between multiple arguments.
print(max(100,200))
print(max(124,2345,3333))
print(max(124,2345,3333,000.55,-1,-2,123.56))

# Minimum Number between multiple arguments.
print(min(100,200))
print(min(124,2345,3333))
print(min(124,2345,3333,000.55,-1,-2,123.56))

# To compare Max and Min , Argument type should be same.
# print(min(124,2345,3333,"Amita"))----> This will gives error because it contains int and string so don't compare
# print(max(124,2345,3333,"Amita"))----> This will gives error because it contains int and string so don't compare


